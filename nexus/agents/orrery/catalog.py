def _render_condition_lines(condition: Any, *, indent: int = 0) -> List[str]:
    """Render a condition as a list of markdown bullet lines."""

    pad = "  " * indent
    if isinstance(condition, CompoundCondition):
        if condition.op == "NOT":
            inner = condition.children[0]
            if isinstance(inner, CompoundCondition):
                return [
                    f"{pad}- **NOT:**",
                    *_render_condition_lines(inner, indent=indent + 1),
                ]
            inner_prose = _render_predicate_name(
                getattr(inner, "__name__", repr(inner))
            )
            return [f"{pad}- **NOT:** {inner_prose}"]
        header = f"{pad}- **{condition.op}:**"
        body: List[str] = []
        for child in condition.children:
            body.extend(_render_condition_lines(child, indent=indent + 1))
        return [header, *body]
    name = getattr(condition, "__name__", repr(condition))
    return [f"{pad}- {_render_predicate_name(name)}"]


def _render_condition_block(condition: Any) -> str:
    """Render a leaf condition as inline text; never call on compounds."""

    name = getattr(condition, "__name__", repr(condition))
    return _render_predicate_name(name)


