def _render_narrative_stub(stub: str) -> List[str]:
    return ["> " + line if line else ">" for line in stub.split("\n")]


def _render_branch(idx: int, branch: Branch) -> List[str]:
    markers = []
    if branch.preemptive:
        markers.append("**preemptive**")
    if not branch.promotable:
        markers.append("**not promotable**")
    marker = f" · {' · '.join(markers)}" if markers else ""
    lines = [
        f"### Branch {idx} — {branch.label}  *(mag {branch.magnitude})*{marker}",
        "",
    ]
    if isinstance(branch.conditions, CompoundCondition):
        lines.extend(["**When:**", ""])
        lines.extend(_render_condition_lines(branch.conditions))
    else:
        lines.append(f"**When:** {_render_condition_block(branch.conditions)}")
    lines.append("")
    if branch.state_delta:
        lines.append(f"**Does:** {_render_state_delta(branch.state_delta)}")
    if branch.event_type:
        lines.append(f"**Event:** `{branch.event_type}`")
    if branch.narrative_stub:
        lines.append("")
        lines.extend(_render_narrative_stub(branch.narrative_stub))
    scene_stub = getattr(branch, "scene_pressure_stub", None)
    if scene_stub:
        lines.append("")
        lines.append(
            "**Scene-pressure prompt** (storyteller-LLM-only; no " "state mutation):"
        )
        lines.append("")
        lines.extend(_render_narrative_stub(scene_stub))
    return lines


def _render_present_target_policy(template: Template) -> str | None:
    """Render the present-target policy as prose, or None if default/missing."""

    policy = getattr(template, "present_target_policy", None)
    if policy is None:
        return None
    value = getattr(policy, "value", str(policy))
    if value == "offscreen_only":
        return None  # default — omit to keep the catalog terse
    if value == "storyteller_pressure":
        return (
            "may target an on-screen entity — but only as a prompt-only "
            "scene-pressure draft routed through storyteller LLM review "
            "(no direct state mutation, no canonical event)"
        )
    return f"`{value}`"


def _render_drive_band(template: Template) -> str:
    """Render one template's drive band and any priority rationale."""

    value = template.drive_band.value.replace("_", " ")
    if template.drive_band_priority_exempt:
        if not template.priority_override_rationale:
            return f"{value} — priority-order exempt"
        return (
            f"{value} — priority-order exempt: {template.priority_override_rationale}"
        )
    if not template.priority_override_rationale:
        return value
    return f"{value} — {template.priority_override_rationale}"


def _render_template(template: Template) -> List[str]:
    slots = ", ".join(s.value.upper() for s in template.required_slots)
    lines = [
        f"## {template.id.upper()} — priority {template.priority}",
        "",
        f"> {template.blurb}",
        "",
        f"**Drive band:** {_render_drive_band(template)}",
        f"**Slots:** {slots}",
    ]
    policy_prose = _render_present_target_policy(template)
    if policy_prose:
        lines.append(f"**Present-target policy:** {policy_prose}")
    lines.append("")
    if isinstance(template.package_gate, CompoundCondition):
        lines.extend(["**Gate:**", ""])
        lines.extend(_render_condition_lines(template.package_gate))
    else:
        lines.append(f"**Gate:** {_render_condition_block(template.package_gate)}")
    lines.append("")
    for i, branch in enumerate(template.branches, start=1):
        lines.extend(_render_branch(i, branch))
        lines.append("")
    return lines


