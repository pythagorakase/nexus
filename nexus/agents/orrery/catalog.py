def render_catalog(templates: Iterable[Template]) -> str:
    """Render the full package catalog as a markdown string."""

    lines = [
        AUTO_GEN_HEADER,
        "",
        "# Orrery Package Catalog",
        "",
        "Behavior templates evaluated by the Orrery off-screen resolver, "
        "ordered by priority (highest first).",
        "",
        "Drive bands are authoring metadata: they explain whether a package is "
        "crisis/constraint, embodied maintenance, anchored routine, affiliation, "
        "or project/identity pressure. Static priority still decides resolver "
        "order; any lower-band package that outranks a higher-band package should "
        "carry an explicit rationale.",
        "",
        "**Source-of-truth:** `nexus/agents/orrery/templates.py` "
        "(`BUILTIN_TEMPLATES`).  ",
        "**Substrate:** `nexus/agents/orrery/substrate.py` "
        "(`Template`, `Branch`, `CompoundCondition`).  ",
        "**Design plan:** [docs/orrery_design_plan.md](orrery_design_plan.md)",
        "",
        "---",
        "",
    ]
    sorted_templates = sorted(templates, key=lambda t: -t.priority)
    warnings = drive_band_priority_warnings(sorted_templates)
    if warnings:
        lines.extend(
            [
                "## Drive-Band Priority Warnings",
                "",
                "These package priorities contradict the default drive-band order "
                "without an explicit rationale:",
                "",
            ]
        )
        for warning in warnings:
            lines.append(f"- {warning}")
        lines.extend(["", "---", ""])
    for template in sorted_templates:
        lines.extend(_render_template(template))
        lines.append("---")
        lines.append("")
    # Pass the already-materialized sorted list, not the original iterable:
    # sorted() exhausts a generator and the appendix would silently render
    # empty.
    lines.extend(_render_vocabulary_appendix(sorted_templates))
    return "\n".join(lines).rstrip() + "\n"


def _doc_path() -> Path:
    """Locate ``docs/orrery_packages.md`` relative to this module.

    catalog.py at nexus/agents/orrery/catalog.py → repo_root/docs/...
    parents[0]=orrery, [1]=agents, [2]=nexus, [3]=repo_root.
    """

    return Path(__file__).resolve().parents[3] / "docs" / "orrery_packages.md"


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    group = parser.add_mutually_exclusive_group()
    group.add_argument(
        "--write",
        action="store_true",
        help="Write the rendered catalog to docs/orrery_packages.md",
    )
    group.add_argument(
        "--check",
        action="store_true",
        help="Exit 1 if docs/orrery_packages.md differs from a fresh render",
    )
    args = parser.parse_args()
    content = render_catalog(BUILTIN_TEMPLATES)
    doc = _doc_path()
    if args.check:
        existing = doc.read_text() if doc.exists() else ""
        if existing != content:
            sys.stderr.write(
                "docs/orrery_packages.md is stale.\n"
                "Run: python -m nexus.agents.orrery.catalog --write\n"
            )
            sys.exit(1)
        return
    if args.write:
        doc.write_text(content)
        sys.stdout.write(f"Wrote {doc}\n")
    else:
        sys.stdout.write(content)


if __name__ == "__main__":
    main()
