_VOCAB_PATTERNS: List[Tuple[str, re.Pattern]] = [
    ("durable_tag", re.compile(r"has_tag\(([^@()]+)@")),
    ("durable_tag", re.compile(r"lacks_tag\(([^@()]+)@")),
    ("durable_tag_list", re.compile(r"has_any_tag\(([^@()]+)@")),
    ("current_tag_list", re.compile(r"has_any_current_tag\(([^@()]+)@")),
    ("ephemeral_tag", re.compile(r"has_ephemeral\(([^@()]+)@")),
    ("pair_tag", re.compile(r"has_pair_tag\(([^@()]+)@")),
    ("pair_tag", re.compile(r"lacks_pair_tag\(([^@()]+)@")),
    ("pair_tag", re.compile(r"has_pair_tag_to_current_location\(([^@()]+)@")),
    ("pair_tag", re.compile(r"has_inbound_pair_tag\(([^@()]+)@")),
    ("pair_tag_list", re.compile(r"has_any_pair_tag\(([^@()]+)@")),
    ("contact_kind", re.compile(r"has_contact_of_kind\(([^@()]+)@")),
    ("event_type", re.compile(r"recent_event\(([^,*()]+),")),
    ("event_type", re.compile(r"knows_recent_event\(([^,*()]+),")),
    ("event_type", re.compile(r"since_last_event_at_least\(([^,()]+),")),
    ("place_class", re.compile(r"in_location_class\(([^@()]+)@")),
    ("place_class_list", re.compile(r"has_location_class_destination\(([^@()]+)@")),
    ("relationship", re.compile(r"has_relationship_of_type\(([^,()]+),")),
    (
        "relationship",
        re.compile(r"has_symmetric_relationship_of_type\(([^,()]+),"),
    ),
]


def _collect_vocabulary(
    templates: Iterable[Template],
) -> dict:
    """Walk templates collecting vocabulary by where it appears.

    Tags queried by gate/branch predicates are classified durable vs
    ephemeral by the predicate that queries them (has_tag → durable,
    has_ephemeral → ephemeral). Tags only appearing in state_delta
    effects are listed in a third "applied" bucket — their durable /
    ephemeral classification lives in the seeding migrations, not in
    the template source, so the renderer doesn't guess.
    """

    durable: set[str] = set()
    ephemeral: set[str] = set()
    current: set[str] = set()
    applied: set[str] = set()
    pair_tags: set[str] = set()
    events: set[str] = set()
    place_classes: set[str] = set()
    relationships: set[str] = set()

    def visit(condition: Any) -> None:
        if isinstance(condition, CompoundCondition):
            for child in condition.children:
                visit(child)
            return
        name = getattr(condition, "__name__", "")
        for kind, pattern in _VOCAB_PATTERNS:
            match = pattern.search(name)
            if not match:
                continue
            captured = match.group(1)
            if kind == "durable_tag":
                durable.add(captured)
            elif kind == "durable_tag_list":
                for tag in captured.split(","):
                    durable.add(tag.strip())
            elif kind == "current_tag_list":
                for tag in captured.split(","):
                    current.add(tag.strip())
            elif kind == "ephemeral_tag":
                ephemeral.add(captured)
            elif kind == "pair_tag":
                pair_tags.add(captured)
            elif kind == "pair_tag_list":
                for tag in captured.split(","):
                    pair_tags.add(tag.strip())
            elif kind == "contact_kind":
                pair_tags.add(f"contact:{captured}")
            elif kind == "event_type":
                events.add(captured)
            elif kind == "place_class":
                place_classes.add(captured)
            elif kind == "place_class_list":
                for tag in captured.split(","):
                    place_classes.add(tag.strip())
            elif kind == "relationship":
                relationships.add(captured)
            # First-match-wins: prevents future overlapping patterns from
            # silently double-classifying a predicate.
            break

    for template in templates:
        visit(template.package_gate)
        for branch in template.branches:
            visit(branch.conditions)
            if branch.event_type:
                events.add(branch.event_type)
            for key, value in branch.state_delta.items():
                if key in (
                    "entity_pair_tags_target.clear_inbound",
                    "entity_pair_tags.add_outbound",
                    "entity_pair_tags.clear_outbound",
                ):
                    if isinstance(value, list):
                        for tag in value:
                            pair_tags.add(str(tag))
                    continue
                if not key.startswith("entity_tags"):
                    continue
                if not isinstance(value, list):
                    continue
                for tag in value:
                    applied.add(tag)

    # Tags appearing in both query and apply contexts shouldn't be
    # duplicated; classification by query wins.
    applied -= durable
    applied -= ephemeral
    applied -= current

    return {
        "durable_tags": sorted(durable),
        "ephemeral_tags": sorted(ephemeral),
        "current_tags": sorted(current),
        "applied_tags": sorted(applied),
        "pair_tags": sorted(pair_tags),
        "event_types": sorted(events),
        "place_classes": sorted(place_classes),
        "relationship_types": sorted(relationships),
    }


def collect_template_vocabulary(templates: Iterable[Template]) -> dict:
    """Return package-facing vocabulary referenced by Orrery templates."""

    return _collect_vocabulary(templates)


def _discover_orrery_migrations() -> List[str]:
    """Find every migration file in migrations/ whose name mentions orrery.

    Globbed rather than hardcoded so renames (or net-new orrery vocabulary
    migrations) keep the catalog cross-reference accurate without manual
    edits. Returns paths relative to repo root, sorted lexically.
    """

    migrations_dir = Path(__file__).resolve().parents[3] / "migrations"
    if not migrations_dir.exists():
        return []
    candidates = sorted(migrations_dir.glob("*orrery*"))
    return [f"migrations/{path.name}" for path in candidates]


def _render_vocabulary_appendix(templates: Iterable[Template]) -> List[str]:
    vocab = _collect_vocabulary(templates)
    lines = [
        "## Vocabulary Reference",
        "",
        "Vocabulary referenced by the templates above. Cross-reference with",
        "the seeding migrations to confirm catalog ↔ schema agreement:",
        "",
    ]
    for migration_path in _discover_orrery_migrations():
        lines.append(f"- `{migration_path}`")
    lines.append("")
    sections = [
        (
            "Tags queried as durable (via `has_tag` / `lacks_tag` / " "`has_any_tag`)",
            vocab["durable_tags"],
        ),
        (
            "Tags queried as ephemeral (via `has_ephemeral`)",
            vocab["ephemeral_tags"],
        ),
        (
            "Tags queried as current durable-or-ephemeral state "
            "(via `has_any_current_tag`)",
            vocab["current_tags"],
        ),
        (
            "Tags applied by branch effects (durable vs ephemeral "
            "classification per migration)",
            vocab["applied_tags"],
        ),
        ("Pair tags queried by directed predicates", vocab["pair_tags"]),
        ("Event types", vocab["event_types"]),
        (
            "Place classes queried by location predicates",
            vocab["place_classes"],
        ),
        ("Relationship types", vocab["relationship_types"]),
    ]
    for heading, values in sections:
        if not values:
            continue
        lines.append(f"### {heading}")
        lines.append("")
        for value in values:
            lines.append(f"- `{value}`")
        lines.append("")
    return lines


