def _render_state_delta(delta: Mapping[str, Any]) -> str:
    parts: List[str] = []
    for key, value in delta.items():
        if key == "character.current_activity":
            parts.append(f'activity → "{value}"')
            continue
        if key in ("entity_tags.add", "entity_tags_target.add"):
            target = "actor" if key == "entity_tags.add" else "target"
            tags = ", ".join(f"`{t}`" for t in value)
            parts.append(f"adds {tags} to {target}")
            continue
        if key in ("entity_tags.remove", "entity_tags_target.remove"):
            target = "actor" if key == "entity_tags.remove" else "target"
            tags = ", ".join(f"`{t}`" for t in value)
            parts.append(f"removes {tags} from {target}")
            continue
        if key == "entity_pair_tags_target.clear_inbound":
            tags = ", ".join(f"`{t}`" for t in value)
            parts.append(f"clears inbound {tags} pair tags from target")
            continue
        if key == "entity_pair_tags.add_outbound":
            tags = ", ".join(f"`{t}`" for t in value)
            parts.append(f"adds outbound {tags} pair tag to target")
            continue
        if key == "entity_pair_tags.clear_outbound":
            tags = ", ".join(f"`{t}`" for t in value)
            parts.append(f"clears own outbound {tags} pair tag to target")
            continue
        if key == "need.fulfill":
            if isinstance(value, Mapping):
                need = value.get("type") or value.get("need")
                quality = value.get("quality")
                discharge = value.get("discharge_debt") or value.get("discharge")
                bits = [f"fulfills `{need}` need"]
                if quality:
                    bits.append(f"quality `{quality}`")
                if discharge is not None:
                    bits.append(f"discharge {discharge}")
                parts.append(", ".join(bits))
            else:
                parts.append(f"fulfills `{value}` need")
            continue
        if key == "travel.start":
            if isinstance(value, Mapping):
                destination_classes = (
                    value.get("destination_place_classes")
                    or value.get("destination_place_class")
                    or value.get("destination_class")
                )
                if destination_classes:
                    if isinstance(destination_classes, str):
                        destination = f"`{destination_classes}`"
                    else:
                        destination = ", ".join(
                            f"`{item}`" for item in destination_classes
                        )
                    parts.append(f"starts travel toward a {destination} destination")
                else:
                    parts.append("starts planned travel")
            else:
                parts.append("starts planned travel")
            continue
        if key == "travel.advance":
            progress = (
                value.get("progress_delta") if isinstance(value, Mapping) else None
            )
            if progress is None:
                parts.append("advances travel")
            else:
                parts.append(f"advances travel by {progress}")
            continue
        if key == "travel.arrive":
            parts.append("arrives at travel destination")
            continue
        if key == "travel.delay":
            parts.append("records travel delay")
            continue
        if key.startswith("project."):
            transition = key.removeprefix("project.")
            parts.append(f"applies project `{transition}` transition")
            continue
        parts.append(f"`{key}` = `{value}`")
    return "; ".join(parts)


