"""Render the Orrery package catalog as human-readable markdown.

The templates in nexus/agents/orrery/templates.py are the canonical
source-of-truth for behavior package definitions. This module introspects
them and emits a structured markdown catalog (docs/orrery_packages.md)
that's easier to read at a glance — for narrative authors drafting new
templates, for reviewers auditing the catalog, and for future contributors
checking what vocabulary is already represented.

The renderer relies on two introspection surfaces:

- ``CompoundCondition`` (substrate.py) exposes ``.op`` and ``.children``
  for AND / OR / NOT, letting the renderer walk gate trees recursively.
- Leaf predicates carry their construction arguments in ``__name__``,
  which the renderer parses back to prose via a regex dispatch table.

Usage:
    python -m nexus.agents.orrery.catalog                # render to stdout
    python -m nexus.agents.orrery.catalog --write        # update docs/
    python -m nexus.agents.orrery.catalog --check        # exit 1 if stale
"""

